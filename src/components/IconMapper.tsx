interface IconProps {
  name: string | null | undefined;
  className?: string;
}

export const Icon: React.FC<IconProps> = ({ name, className }) => {
  const LucideIconComponent = name ? iconMap[name] : null;
  
  if (!LucideIconComponent) {
    return <Component className={className} />; // Render a default icon if name is null or not found
  }
  
  return <LucideIconComponent className={className} />;
};